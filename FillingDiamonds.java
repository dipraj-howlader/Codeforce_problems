import java.util.Scanner;

public class FillingDiamonds {
    public static void main(String[] args) {
        Scanner iScanner = new Scanner(System.in);
        int test = iScanner.nextInt();
        while(test>0){
            test--;
            long n = iScanner.nextLong();
            System.out.println(n);
        }
    }
}
