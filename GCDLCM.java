import java.util.Scanner;

public class GCDLCM {
    public static void main(String[] args) {
        Scanner iScanner = new Scanner(System.in);
        int test = iScanner.nextInt();
        while(test>0){
            long n = iScanner.nextLong();
            System.out.println(n-3 +" "+"1"+" "+"1"+" "+"1");
            test--;
        }
    }
}
